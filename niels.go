//XXX SECURITY this lookup should be done in constant time as it is on the
// original code
func (table *fixedTable) decafLookup(t, idx uint) *bigNumber {
	nin := 1 << (t - 1)    // j is always 1 now
	in := table.base[nin:] //this is not constant time
	return in[idx].copy()
}

